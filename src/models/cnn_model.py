from __future__ import annotations

import torch
import torch.nn as nn

from .base_model import BaseModel


class CNNClassifier(BaseModel):
    """A simple CNN consisting of a few convolutional blocks followed by a
    linear classifier. The architecture is intentionally lightweight so that it
    can be trained on modest hardware.
    """

    def __init__(self, num_classes: int) -> None:
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(1, 16, kernel_size=3, padding=1),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2),
            nn.Conv2d(16, 32, kernel_size=3, padding=1),
            nn.ReLU(inplace=True),
            nn.MaxPool2d(2),
        )
        self.classifier = nn.Sequential(
            nn.AdaptiveAvgPool2d((1, 1)),
            nn.Flatten(),
            nn.Linear(32, num_classes),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = self.features(x)
        x = self.classifier(x)
        return x


__all__ = ["CNNClassifier"]